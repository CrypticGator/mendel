var Dissolve = require("dissolve");
var URLSafeBase64 = require('urlsafe-base64');

var invalidHash = new Error("Invalid hash");
invalidHash.code = "BADHASH";

function deserialize(treeHash) {
    var result = {
        error: null
    };

    var branches = [];
    var parser = new Dissolve()
        .string('name', 6)
        .tap(function() {
            if (this.vars.name !== 'mendel') {
                invalidHash.message += ": not generated by mendel";
                throw invalidHash;
            }
        })
        .uint8('version')
        .tap(function() {
            if (this.vars.version !== 1) {
                invalidHash.message += ": version mismatch";
                throw invalidHash;
            }
        })
        .loop('branches', function(end) {
            this.uint8('data').tap(function() {
                if (this.vars.data === 255) {
                    return end();
                }
                branches.push(this.vars.data);
            })
        })
        .tap(function() {
            this.vars.branches = branches;
        })
        .uint16('files')
        .buffer('hash', 20)
        .tap(function() {
            result.decoded = this.vars;
        });

    var binary = URLSafeBase64.decode(treeHash);
    parser.write(binary);

    var ok = result.decoded && Buffer.isBuffer(result.decoded.hash);
    if (!ok) {
        result.error = invalidHash;
    }

    return result;
}

module.exports = deserialize;
